

def as_red(s):
    '''
        As red text
    '''
    return '\033[31m' + s + '\033[0m'


def as_blue(s):
    '''
        As blue text
    '''
    return '\033[34m' + s + '\033[0m'


def as_green(s):
    '''
        As green text
    '''
    return '\033[32m' + s + '\033[0m'


def as_yellow(s):
    '''
        As yellow text
    '''
    return '\033[33m' + s + '\033[0m'

